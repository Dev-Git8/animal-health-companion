// Helper function to get translation
export const getTranslation = (language, key) => {
  const keys = key.split(".");
  let result = translations[language] || translations.en;
  
  for (const k of keys) {
    result = result?.[k];
  }
  
  return result || translations.en[keys[keys.length - 1]] || key;
};
